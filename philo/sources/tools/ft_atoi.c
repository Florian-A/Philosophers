#include "../../includes/philosophers.h"

int	ft_atoi(const char *str)
{
	int		i;
	long	n;
	int		s;

	i = 0;
	n = 0;
	s = 1;
	while (str[i])
	{
		if (ft_isdigit(str[i]) || str[i] == '+')
			i++;
		else
			return (0);
	}
	i = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		if ((n * 10 + (str[i] - '0')) / 10 != n && s == -1)
			return (0);
		else if ((n * 10 + (str[i] - '0')) / 10 != n)
			return (0);
		n = n * 10 + str[i] - '0';
		i++;
	}
	return ((int)(n * s));
}
